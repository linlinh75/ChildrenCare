package model;

/**
 *
 * @author Admin
 */
public class DateToMap {
    
    private int id;
    private String date;

    public DateToMap(int id, String date) {
        this.id = id;
        this.date = date;
    }

    // Getters and setters
    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getDate() {
        return date;
    }

    public void setDate(String date) {
        this.date = date;
    }
}


