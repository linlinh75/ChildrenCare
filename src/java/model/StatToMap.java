package model;

/**
 *
 * @author Admin
 */
public class StatToMap {
    private int id;
    private String name;
    private int quantity;
    
    public StatToMap(int id,String name, int quantity) {
        this.id = id;
        this.quantity = quantity;
        this.name = name;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    // Getters and setters
    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public int getQuantity() {
        return quantity;
    }

    public void setQuantity(int quantity) {
        this.quantity = quantity;
    }
}
